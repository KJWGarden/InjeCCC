-- ============================================================
-- 족보(Genealogy) 테이블 생성 + 시드 데이터
-- Supabase SQL Editor 에서 실행
-- ============================================================

-- 1. 멤버 테이블
create table if not exists genealogy_members (
  id          text primary key,           -- "01_배경태" 형식
  name        text        not null,
  student_id  text        not null,
  level       text        not null,       -- 나사렛 / 순장
  team        text        not null,       -- 제자
  color       text        not null,       -- hex color
  join_year   smallint    not null,
  depth       smallint    not null default 1,
  created_at  timestamptz not null default now()
);

-- 2. 엣지(멘토→멘티) 테이블
create table if not exists genealogy_edges (
  id          bigint generated always as identity primary key,
  source_id   text not null references genealogy_members(id) on delete cascade,
  target_id   text not null references genealogy_members(id) on delete cascade,
  created_at  timestamptz not null default now(),
  unique (source_id, target_id)
);

-- 인덱스: source/target 별 빠른 조회
create index if not exists idx_genealogy_edges_source on genealogy_edges(source_id);
create index if not exists idx_genealogy_edges_target on genealogy_edges(target_id);

