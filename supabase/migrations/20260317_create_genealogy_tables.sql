-- 5. RLS (Row Level Security) - 읽기 전용 공개
-- ============================================================
alter table genealogy_members enable row level security;
alter table genealogy_edges   enable row level security;

create policy "genealogy_members_read" on genealogy_members
  for select using (true);

create policy "genealogy_edges_read" on genealogy_edges
  for select using (true);

-- ============================================================
-- 6. 유용한 뷰: edgesBySource 와 동일한 JSON 구조 생성
-- ============================================================
create or replace view genealogy_edges_by_source as
select
  source_id,
  array_agg(target_id order by target_id) as targets
from genealogy_edges
group by source_id;
